    public void populateOrganizationComboBox() {
        organization1JComboBox.removeAllItems();

        for (Organization organization : organizationDir.getOrganizationList()) {
            organization1JComboBox.addItem(organization);
        }
    }

    public void populateOrganizationEmpComboBox() {
        organizationEmp1JComboBox.removeAllItems();

        for (Organization organization : organizationDir.getOrganizationList()) {
            organizationEmp1JComboBox.addItem(organization);
        }
    }

    private void populateTable(Organization organization) {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();

        model.setRowCount(0);

        for (Employee employee : organization.getEmployeeDirectory().getEmployeeList()) {
            Object[] row = new Object[2];
            row[0] = employee.getId();
            row[1] = employee.getName();
            model.addRow(row);
        }
    }
                       

    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {                                           
        if (!nameJTextField.getText().equals("")) {
            Organization organization = (Organization) organizationEmp1JComboBox.getSelectedItem();
            String name = nameJTextField.getText();
            organization.getEmployeeDirectory().createEmployee(name);
            populateTable(organization);
        } else {
            JOptionPane.showMessageDialog(null, "Enter value", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }                                          

    private void organizationJComboBoxActionPerformed(java.awt.event.ActionEvent evt) {                                                      
        Organization organization = (Organization) organization1JComboBox.getSelectedItem();
        if (organization != null) {
            populateTable(organization);
        }
    }                                             
