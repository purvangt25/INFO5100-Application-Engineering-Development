    public void populateSiteTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblSite.getModel();
        dtm.setRowCount(0);
        for (Scene site : system.getSceneList().getSceneList()) {

            Object[] row = new Object[7];
            row[0] = site;
            row[1] = site.getSceneId();
            row[2] = site.getArea_affected();
            row[3] = site.getLoss_estimation();
            row[4] = site.getNum_victims();
            row[5] = site.getZipcode();
            row[6] = site.getLatLong();
            dtm.addRow(row);
        }
    }
