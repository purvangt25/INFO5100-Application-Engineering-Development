    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:
        if (!txtSiteName.getText().equals("")) {
            Scene s = system.getSceneList().addScene();
            s.setSceneName(txtSiteName.getText());
            s.setArea_affected(txtAffectedArea.getText());
            s.setNum_victims(Integer.parseInt(txtNumberOfVictims.getText()));
            s.setLoss_estimation(txtEstimatedLoss.getText());
            s.setZipcode(Integer.parseInt(txtZipCode.getText()));
            s.setLatLong(position);

        } else {
            JOptionPane.showMessageDialog(null, "Enter value", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        populateSiteTable();

    }//GEN-LAST:event_btnNewActionPerformed

